    pred = model.predict([[today, p_pop]])
    st.success(f"AI ‡∏Ñ‡∏≤‡∏î‡∏Å‡∏≤‡∏£‡∏ì‡πå‡∏ß‡πà‡∏≤‡∏à‡∏∞‡∏°‡∏µ‡∏Ç‡∏¢‡∏∞: {pred[0]:,.2f} kg")